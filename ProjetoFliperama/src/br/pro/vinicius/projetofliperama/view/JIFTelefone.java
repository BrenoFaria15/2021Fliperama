public boolean validarCampos(){
    if(jtTel.getText().length()<2){
        JOptionPane.showMessageDialog(this, "Campo em branco ou inv√°lido");
        return false;
    }
    return true;
}    
 
    private boolean preencherObjeto()throws Exception{
    obj.setIdtelefone(Integer.parseInt(jlId2.getText()));
    obj.setJogador(listaJ.get(jcbPessoa.getSelectedIndex()-1));
    obj.setTelefone(jtTel.getText());
    
    return true;
}
