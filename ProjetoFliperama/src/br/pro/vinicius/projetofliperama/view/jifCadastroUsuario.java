public boolean validarCampos(){
    if(jtLogin.getText().length() == 0){
        JOptionPane.showMessageDialog(this, "Digite o login");
        return false;
    }
    if(jpfSenha.getText().length() == 0){
        JOptionPane.showMessageDialog(this, "Digite a senha");
        return false;
    }
    return true;
}

    private boolean preencherObjeto()throws Exception{
    obj.setLogin(jtLogin.getText());
    obj.setSenha(jpfSenha.getText());   
    return true;
}

