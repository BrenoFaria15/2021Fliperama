    private boolean preencherObjeto()throws Exception{
    obj.setIdemprestimo(Integer.parseInt(jlId2.getText()));
    obj.setJogo(listaJg.get(jcbJogo.getSelectedIndex()-1));
    obj.setJogador1(listaJ.get(jcbJogador.getSelectedIndex()-1));
    obj.setDtaAluga(new Timestamp(jtEmp.getDate().getTime()));
    obj.setDtaDevolve(new Timestamp(jtDataDev.getDate().getTime()));

    
    return true;
}


