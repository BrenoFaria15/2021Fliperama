    private void jtPesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtPesquisaKeyReleased
        try {
        
        lista = DAO.pesquisar(jtPesquisa.getText());
        DefaultTableModel mp = (DefaultTableModel) jtbDados.getModel();
        mp.setNumRows(0);
        if(jtPesquisa.getText().length()>1){
        for(JogoModel ob: lista){
            mp.addRow(new String[]{""+ob.getIdJogo(),ob.getNome(),ob.getDesenvolvedora(), ""+ob.getAnoLanc(), ""+ob.getPlataforma().getDescricao(), ""+ob.getGenero().getDescricao()});
        }
        }
    } catch (Exception e) {
       JOptionPane.showMessageDialog(this, "Ocorreu um erro: "+e.getMessage());
    }
    }//GEN-LAST:event_jtPesquisaKeyReleased

    private void jtbDadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbDadosMouseClicked
        try {
            int linha = jtbDados.getSelectedRow();
            if(linha>-1){
                jlId2.setText(jtbDados.getValueAt(linha, 0).toString());
                jtNome.setText(jtbDados.getValueAt(linha, 1).toString());
                jtDesen.setText(jtbDados.getValueAt(linha, 2).toString());
                jtAno.setText(jtbDados.getValueAt(linha, 3).toString());
                jcbPlataforma.setSelectedItem(jtbDados.getValueAt(linha, 4));
                jcbGenero.setSelectedItem(jtbDados.getValueAt(linha, 5));
                jbEditar.setEnabled(true);
                jbExcluir.setEnabled(true);
            } else {
                jbEditar.setEnabled(false);
                jbExcluir.setEnabled(false);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao selecionar o registro: "+e.getMessage());
        }
    }//GEN-LAST:event_jtbDadosMouseClicked

