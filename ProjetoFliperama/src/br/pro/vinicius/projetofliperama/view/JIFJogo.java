public boolean validarCampos(){
    if(jtNome.getText().length()<3){
        JOptionPane.showMessageDialog(this, "Campo em branco ou inválido");
        return false;
    }if(jtDesen.getText().length()<2){
        JOptionPane.showMessageDialog(this, "Campo em branco ou inválido");
        return false;
    
    }
    return true;
}    
 
    private boolean preencherObjeto()throws Exception{
    obj.setIdJogo(Integer.parseInt(jlId2.getText()));
    obj.setNome(jtNome.getText());
    obj.setDesenvolvedora(jtDesen.getText());
    obj.setAnoLanc(Integer.parseInt(jtAno.getText()));
    obj.setPlataforma(listaP.get(jcbPlataforma.getSelectedIndex()-1));
    obj.setGenero(listaG.get(jcbGenero.getSelectedIndex()-1));
    
    return true;
}

