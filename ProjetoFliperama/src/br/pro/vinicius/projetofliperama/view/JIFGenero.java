public boolean validarCampos(){
    if(jtGenero.getText().length()<3){
        JOptionPane.showMessageDialog(this, "Digite o Genêro");
        return false;
    }
    return true;
}
 
    private boolean preencherObjeto()throws Exception{
    obj.setId(Integer.parseInt(jlId2.getText()));
    obj.setDescricao(jtGenero.getText());
    return true;
}

