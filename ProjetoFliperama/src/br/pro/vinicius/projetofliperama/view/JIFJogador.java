public boolean validarCampos(){
    if(jtNome.getText().length()<3){
        JOptionPane.showMessageDialog(this, "Campo em branco ou inválido");
        return false;
    }if(jtRua.getText().length()<2){
        JOptionPane.showMessageDialog(this, "Campo em branco ou inválido");
        return false;
    
    }
    return true;
}    
