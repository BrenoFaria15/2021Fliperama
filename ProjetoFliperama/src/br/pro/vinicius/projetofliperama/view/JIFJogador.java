    private boolean preencherObjeto()throws Exception{
    obj.setIdjogador(Integer.parseInt(jlId2.getText()));
    obj.setNome(jtNome.getText());
    obj.setNasc(new Timestamp(jdNasc.getDate().getTime()));
    obj.setEmail(jtEmail.getText());
    obj.setRua(jtRua.getText());
    obj.setNro_casa(Integer.parseInt(jtNumero.getText()));
    obj.setBairro(jtBairro.getText());
    return true;
}

